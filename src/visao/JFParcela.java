    private void jBConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConcluirActionPerformed
        if (verificaCampos()) {
            insereParcelas();
            if (inserirMaisParcela()) {
                parcelas++;
                limpaData();
            } else {
                JFVenda jfvenda = new JFVenda();
                jfvenda.limpaCamposCliente();
                jfvenda.limpaCamposProduto();
                this.setVisible(false);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Os campos Data de Vencimento e Valor da Parcela são obrigatorios");
        }
    }//GEN-LAST:event_jBConcluirActionPerformed

