    private void simularParcela() {
        double venda, parcela;
        int NumParcela;
        venda = Double.valueOf(jTValorVenda.getText());
        NumParcela = Integer.valueOf(jTNumParcela.getText());
        parcela = (venda / NumParcela);
        jTValorParcela.setText(String.valueOf(parcela));
    }

    private boolean verificaCampos() {
        boolean retorno;
        if (!jTNumParcela.getText().equals("") && !jTValorVenda.getText().equals("")) {
            retorno = true;
        } else {
            retorno = false;
        }
        return retorno;
    }

    private void setVendaParcela() {
        Venda venda = new Venda();
        VendaDAO dao = new VendaDAO();
        venda = dao.buscaCodigoValor();
        jTCodigoVenda.setText(String.valueOf(venda.getId()));
        jTValorVenda.setText(String.valueOf(venda.getValorVenda()));
    }

    private void limpaData() {
        jDateVencimento.setCalendar(null);
        jTCodigoParcela.setText(String.valueOf(parcelas));
    }

    private void insereParcelas() {
        Parcela parcela = new Parcela();
        parcela.setDataVencimento(pegaData());
        parcela.setValorParcela(Double.valueOf(jTValorParcela.getText()));
        parcela.setVenda(Integer.valueOf(jTCodigoVenda.getText()));
        ParcelaDAO dao = new ParcelaDAO();
        dao.inserir(parcela);
    }

    private String pegaData() {
        int dia, mes, ano;
        String data = null;
        ano = jDateVencimento.getCalendar().get(Calendar.YEAR);
        mes = jDateVencimento.getCalendar().get(Calendar.MARCH);
        dia = jDateVencimento.getCalendar().get(Calendar.DAY_OF_MONTH);
        data = String.valueOf(ano + "/" + mes + "/" + dia);
        return data;
    }

    private boolean inserirMaisParcela() {
        JOptionPane.showMessageDialog(null, "Parcela incluida com sucesso !");
        int resp = JOptionPane.showConfirmDialog(this, "Deseja inserir mais uma parcela à essa venda",
                "Confirmação", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_NO_OPTION) {
            return true;
        } else {
            return false;
        }
    }

    private void limpaCampos() {
        jDateVencimento.setCalendar(null);
        jTNumParcela.setText("");
        jTValorParcela.setText("");
    }
